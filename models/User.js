import mongoose, { Schema } from "mongoose";

const UserSchema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  }
});

const model = mongoose.model("User", UserSchema);

export default model;
